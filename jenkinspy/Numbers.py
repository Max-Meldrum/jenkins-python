"""Numbers module"""


class Math(object):
    """Math class that is used for the testing."""

    @staticmethod
    def add(num_1, num_2):
        """Return sum of first and second argument"""
        return num_1+num_2

    @staticmethod
    def sub(num_1, num_2):
        """Return the result of the subtraction of x and y"""
        return num_1-num_2

    @staticmethod
    def mult(num_1, num_2):
        """Return the result of the multiplication"""
        return num_1-num_2
